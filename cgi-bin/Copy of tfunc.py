import re,textwrap
def getPartNum (line,partNum) :
    line=line.strip()
    if len(line) != 0 and line[0] is "[" :
        z=line.split("]")[0][1:]
        if not re.search("[^๐-๙0-9]", z) :
            return z
        else :
            return partNum      
    else :
        return partNum
        
def getTitle(line,title) :
    if  line.find("<h")>-1 :
        return re.sub("<.*?>", "",line)
    else :
        return title

def specialChar(line):
    return line.replace("ฃ","ญ").replace("ฅ","ฐ")

def wrapText(p, leng=60) :
    l=[]
    last=0
    for i in range(1, len(p)/leng):
        l.append(p[last:leng*i])
        last=leng*i
    l.append(p[last:len(p)])
    return l
        
